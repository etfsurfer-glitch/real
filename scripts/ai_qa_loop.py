def main():
    start = int(sys.argv[1]) if len(sys.argv) > 1 else 1
    count = int(sys.argv[2]) if len(sys.argv) > 2 else 8
    log = sqlite3.connect("data/logs.sqlite")
    for i in range(start, min(start + count, len(QUESTIONS) + 1)):
        q = QUESTIONS[i - 1]
        t = time.time()
        try:
            r = AG.run_agent(q)
        except Exception as e:
            r = {"answer": f"[ERROR] {e}", "tools_used": [], "usage": {}}
        el = time.time() - t
        tools = "; ".join(f"{x['tool']}({','.join(f'{k}={v}' for k,v in x['args'].items())})" for x in r.get("tools_used", []))
        print(f"\n────── Q{i}: {q}")
        print(f"도구: {tools or '(없음)'}")
        print(f"답변: {(r.get('answer') or '').strip()[:400]}")
        print(f"({el:.1f}s, 토큰 {r.get('usage',{}).get('total_tokens')})")
        try:
            log.execute("INSERT INTO event_log(ts,kind,detail) VALUES(datetime('now'),'ai_qaloop',?)",
                        (json.dumps({"n": i, "question": q, "answer": r.get("answer"), "tools": tools}, ensure_ascii=False),))
            log.commit()
        except Exception:
            pass
    log.close()

if __name__ == "__main__":
    main()
